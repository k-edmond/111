const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Language>('en');

  const t = (key: string): string => {
    return translations[lang][key] || key;
  };

  // Apply RTL for Arabic and adjust fonts for other languages
  useEffect(() => {
    if (typeof document !== 'undefined') {
      // Remove all language-specific classes
      document.documentElement.classList.remove('lang-ar', 'lang-ja', 'lang-ru', 'lang-en', 'lang-tr', 'lang-zhHK');
      
      // Add appropriate class based on selected language
      if (lang === 'ar') {
        document.documentElement.classList.add('lang-ar');
        document.documentElement.setAttribute('dir', 'rtl');
      } else if (lang === 'zh-HK') {
        document.documentElement.classList.add('lang-zhHK');
        document.documentElement.setAttribute('dir', 'ltr');
      } else {
        document.documentElement.setAttribute('dir', 'ltr');
      }
    }
  }, [lang]);

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
